# ============================================================
# 类型 2：函数定义——import 时也执行（注册函数到内存）
# ============================================================
# Python 读到这里时会执行 def 语句，把 triple 注册到内存里。
# 但函数体里的 print 不会在这时执行——等调用时才会执行。


def triple(x):
    print(f"    [函数体内] triple({x}) 正在计算...")
    return x * 3


print("[顶级代码] def triple 语句也被执行了——函数已注册到内存，但还没被调用")

# ============================================================
# 类型 3：创建变量——import 时一定执行
# ============================================================
FAVORITE_NUMBER = 42
print(f"[顶级代码] 变量 FAVORITE_NUMBER = {FAVORITE_NUMBER} 被创建了")

# ============================================================
# 类型 4：if 判断本身也执行，但 if 块里面的代码不一定
# ============================================================
print(f"[顶级代码] 当前 __name__ = {__name__!r}，即将判断...")

if __name__ == "__main__":
    # ↓ 以下四行：只有 python playground_lib.py 时才会执行
    print("")
    print("==============================================")
    print("  我是主角！直接运行时才能看到我")
    print(f"  测试 triple(7) = {triple(7)}")
    print("==============================================")
    print("")
else:
    # ↓ 被 import 时走这里
    print("[if 的 else 分支] 我是被 import 的，跳过测试")

print("[顶级代码] 判断结束，文件末尾。\n")
